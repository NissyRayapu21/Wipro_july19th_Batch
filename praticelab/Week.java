package praticelab;

public class Week {

	public static void main(String[] args) {
		
		int number=1;
		switch(number) {
		
		case 1:
			System.out.println("it is monday");
			break;
		case 2:
		    System.out.println("is is tuesday");
		    break;
		case 3 :
			System.out.println("it is wed");
			break;
		case 4:
		    System.out.println("is is thursday");
		    break;
		case 5:
		    System.out.println("is is friday");
		    break;
		case 6:
		    System.out.println("is is sat");
		    break;    
			default:
				System.out.println("no day");
		}
				
		
	}

}
